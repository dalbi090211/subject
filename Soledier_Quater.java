public class Soledier_Quater {
    int rank, hoghangi;
    Soledier_Quater(int rank, int hoghangi){
        super();
        this.rank = rank;
        this.hoghangi = hoghangi;
    }
    public void setRank(int rank) {
        this.rank = rank;
      }
      public void setName(String name) {
        this.name = name;
      }
      public int getHoghangi() {
        return hoghangi;
      }
    
      public void setHoghangi(int hoghangi) {
        this.hoghangi = hoghangi;
      }

}
